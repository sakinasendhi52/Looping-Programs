// 9. Find the sum of numbers from 1 to 10.

console.log(`-------------------- Sum of numbers from 1 to 10 --------------------`);

let i:number=1, sum:number=0;                    //variable
while(i<=10){                                    //while loop
    sum+=i;                                      //add number
    i++;                                         //variable increment
}

console.log(`The sum of numbers from 1 to 10 is ${sum}.`);        //print output

